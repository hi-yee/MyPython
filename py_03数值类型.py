# 数值类型
# 1、整型
print(6655545 + 4455454)

# 2、浮点型
print(0.25656565)
print(2.5e-21)

# 3、布尔类型 ,用True和FALSE来表示真假，布尔类型可以当做整数来看，True相当于整数值：1；False相当于整数值：0
print(True + True)
print(False - True)

#  类型转换
# int() 的作用是将一个字符串或者浮点数转换成一个整数
# float()的作用是将一个字符串或整数转换成一个浮点数（就是小数）
# str（）的作用是将一个数或任何其他类型转成一个字符串
a = '520'
b = int(a)
c = 1314.572
d = int(c)
g = '25'
f = float(g)
print(b)
print(c)
print(f)
